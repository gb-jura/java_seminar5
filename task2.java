import java.util.HashMap;
import java.util.Map;
import java.util.List;
import java.util.Arrays;
import java.util.ArrayList;

public class Main {

    public static void main(String[] args) {

        List<String> employees = Arrays.asList(
                "???? ??????",
                "???????? ???????",
                "???????? ??????",
                "???? ??????",
                "???? ???????",
                "???? ????",
                "???? ?????",
                "????? ??????",
                "???? ????????",
                "????? ????????",
                "?????? ????????",
                "????? ??????",
                "????? ??????",
                "?????? ??????",
                "???? ???????????",
                "???? ????????",
                "???? ?????",
                "???? ????");

        Map<String, Integer> nameCount = new HashMap<>();

        for (String employee : employees) {
            String[] names = employee.split(" ");
            String name = names[0];
            Integer count = nameCount.get(name);
            if (count == null) {
                count = 0;
            }
            nameCount.put(name, count + 1);
        }

        List<Map.Entry<String, Integer>> sortedByNameCount =
                new ArrayList<>(nameCount.entrySet());
        sortedByNameCount.sort(Map.Entry.<String, Integer>comparingByValue().reversed());

        for (Map.Entry<String, Integer> entry : sortedByNameCount) {
            if (entry.getValue() > 1) {
                System.out.println(entry.getKey() + ": " + entry.getValue());
            }
        }
    }
}