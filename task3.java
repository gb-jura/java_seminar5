public class ChessBoard {
    private int[][] board;
    private int size;

    public ChessBoard(int size) {
        this.size = size;
        board = new int[size][size];
    }

    private boolean isCellSafe(int row, int col) {
        // ????????? ??????????? ? ?????????
        for (int i = 0; i < size; i++) {
            if ((board[row][i] == 1) || (board[i][col] == 1)) {
                return false;
            }
        }

        // ????????? ?????????
        for (int i = 0; i < size; i++) {
            for (int j = 0; j < size; j++) {
                if (((i + j) == (row + col)) || ((i - j) == (row - col))) {
                    if (board[i][j] == 1) {
                        return false;
                    }
                }
            }
        }
        return true;
    }

    private boolean placeQueen(int col) {
        // ???? ??????? ?????????? ???x ??????
        if (col == size) {
            return true;
        }

        // ???????? ?? ???? ??????? ??????? ???????
        for (int row = 0; row < size; row++) {
            // ???? ?????? ? ???? ??????? ?????????
            if (isCellSafe(row, col)) {
                // ??????????? ?????
                board[row][col] = 1;

                // ???????? ?????????? ????????? ????? ? ????????? ????????
                if (placeQueen(col+1)) {
                    return true;
                }

                // ???? ?????????? ????? ? ???? ??????? ?? ???????, ?? ????????
                board[row][col] = 0;
            }
        }

        // ???? ?? ???? ?? ?????? ? ???? ??????? ?? ???????, ?????????? false
        return false;
    }

    public void printBoard() {
        for (int i = 0; i < size; i++) {
            for (int j = 0; j < size; j++) {
                if (board[i][j] == 1) {
                    System.out.print("X ");
                } else {
                    System.out.print("0 ");
                }
            }
            System.out.println();
        }
    }

    public static void main(String[] args) {
        ChessBoard board = new ChessBoard(8);
        board.placeQueen(0);
        board.printBoard();
    }
}